# Listas, Tuplas, Sets, Strings, y Diccionarios.
# Se pueden recorrer utilizando bucles for.

for elemento in [1, 2, 3, 4]:
    print(elemento)

# Resultado: 
#
# 1
# 2
# 3
# 4

for elemento in range(5):
    print(elemento)

# Resultado:
#
# 0
# 1
# 2
# 3
# 4

for elemento in "Hola":
    print(elemento)

# Resultado:
#
# H
# o
# l
# a
