ciudades = ["Barcelona", "París", "Roma", "Londres", "Berlín"]

print(ciudades)

# Resultado:
#
# ['Barcelona', 'París', 'Roma', 'Londres', 'Berlín']
#

ciudades.reverse()

print(ciudades)

# Resultado:
# ['Berlín', 'Londres', 'Roma', 'París', 'Barcelona']
#

print(list(reversed(ciudades)))
print(ciudades)

# Resultado:
#
# ['Barcelona', 'París', 'Roma', 'Londres', 'Berlín']
